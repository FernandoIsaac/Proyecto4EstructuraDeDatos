    private void EliminarProductoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EliminarProductoMouseClicked
        // TODO add your handling code here:
        if (productos.isEmpty()) {

            MessageDialog.pack();
            MessageDialog.setModal(true);
            MessageDialog.setLocationRelativeTo(null);
            TextoMensaje.setText("No hay Productos para eliminar");
            ImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/puppy.jpg")));
            MessageDialog.setVisible(true);

        } else {
            for (int i = 0; i < productos.size(); i++) {
                if (cb_eliminarProducto.getSelectedItem().equals(productos.peek())) {
                    productos.remove();
                } else {
                    tempProductos.add(productos.remove());

                }
            }
            for (int i = 0; i < tempProductos.size(); i++) {
                productos.add(tempProductos.poll());
            }
            cb_eliminarProducto.removeItemAt(cb_eliminarProducto.getSelectedIndex());
            cb_modificarProducto.removeItemAt(cb_eliminarProducto.getSelectedIndex() + 1);
            cb_AgregarProductosOrdenes.removeItemAt(cb_eliminarProducto.getSelectedIndex() + 1);
            
            MessageDialog.pack();
            MessageDialog.setModal(true);
            MessageDialog.setLocationRelativeTo(null);
            TextoMensaje.setText("Se Elimino El Producto");
            ImageLabel.setIcon(new javax.swing.ImageIcon(getClass().getResource("/Images/panadera.jpg")));
            MessageDialog.setVisible(true);
        }
    }//GEN-LAST:event_EliminarProductoMouseClicked

